        (function() {
            // Function to update button values using the mapping
            function applyGameLabels() {
                const buttons = document.querySelectorAll('.buttons-container input[type="button"]');
                buttons.forEach(btn => {
                    const original = btn.value;
                    if (window.gameLabels && window.gameLabels[original]) {
                        btn.value = window.gameLabels[original];
                    }
                });
            }

            // Run once after games.js has loaded
            applyGameLabels();

            // Also handle the "lolbutton"  when it loads games.js again, we need to reapply
            const lolButton = document.getElementById('lolbutton');
            if (lolButton) {
                const originalOnClick = lolButton.onclick;
                lolButton.onclick = function(e) {
                    // Call the original onclick (which loads the script)
                    originalOnClick.call(this, e);
                    // After a short delay, reapply labels (new buttons will appear)
                    setTimeout(applyGameLabels, 200);
                };
            }
        })();
